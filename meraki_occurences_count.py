l=["a","n","t","a","a","t","n","n","a","x","u","a","x","u","g","a","x","a"]
i=0
while i<len(l):
    a=l.count("a")
    b=l.count("n")
    c=l.count("t")
    d=l.count("x")
    e=l.count("u")
    f=l.count("g")
    i=i+1
print("a -",a,"time")
print("n -",b,"time")
